int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (i < n && (s2[i] || s1[i]))
	{
		if (s1[i] != s2[i])
			return ((int)s1[i] - s2[i]);
		i++;
	}
	return (0);
}
