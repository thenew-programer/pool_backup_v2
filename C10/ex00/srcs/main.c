#include "ft.h"

int	main(int argc, char **argv)
{
	int	fd;

	if (argc < 2)
	{
		ft_putstr("File name missing.\n");
		return (1);
	}
	if (argc > 2)
	{
		ft_putstr("Too many arguments.\n");
		return (1);
	}
	fd = open(argv[1], O_RDONLY);
	if (fd < 3)
	{
		ft_putstr("Cannot read file.\n");
		return (1);
	}
	read_file(fd);
	close(fd);
	return (0);
}
