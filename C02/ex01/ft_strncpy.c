char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int		i;

	i = 0;
	while (i < n && src[i])
	{
		dest[i] = src[i];
		i++;
	}
	if (i < sizeof(dest))
	{
		while (i < sizeof(dest))
			dest[i++] = '\0';
	}
	return (dest);
}
